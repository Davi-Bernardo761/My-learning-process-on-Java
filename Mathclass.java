public class Mathclass {
    public static void main (String[] args){
        double number = 0;
        number = Math.pow(2,4);
        number = Math.sqrt(16);
        number = Math.abs(-9);
        number = Math.ceil(1.4);
        number = Math.round(7.8);
        number = Math.floor(1.9);
        number = Math.max(20,100);
        number = Math.min(1,5);
        System.out.println(number);
    }
}
